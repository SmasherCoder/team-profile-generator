// push array to page 
generateHTML = (data) => {
    // array for cards 
    pageArray = []; 

    //for loop to loop through array of employees to generate cards for html page
    for (let i = 0; i < data.length; i++) {
        const employee = data[i];
        const role = employee.getRole(); 

        // generate manager card function
        if (role === 'Manager') {
            const managerCard = generateManager(employee);

            pageArray.push(managerCard);
        }

        // generate engineer card function
        if (role === 'Engineer') {
            const engineerCard = generateEngineer(employee);

            pageArray.push(engineerCard);
        }

        // generate intern card function 
        if (role === 'Intern') {
            const internCard = generateIntern(employee);

            pageArray.push(internCard);
        }
    }

    const employeeCards = pageArray.join('')

    // returns generated cards from array of employees
    const generateTeam = generateTeamPage(employeeCards); 
    return generateTeam;
};

